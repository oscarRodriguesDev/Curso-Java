package br.com.aed.Logica;

public class Constantes {

	/** Classe se destina a exemplificar uso, de constatnes */
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		/** constatntes sao valores que nao podem ser modificados no programa */
		/*
		 * valor pode ser modificado pois a população muda a cada segundo,
		 * portanto deve ser armazenado dentro de uma variavel
		 */
		int população = 21645516;

		/*
		 * numero pi, para criarmos a constante devemos ultiizar a palvra final
		 * antes do tipo da variavel
		 */
		final double PI = 3.14159265358979323846;
		/*
		 * constantes sao nomeadas sempre em letra maiusculas se for nome
		 * composto usar underline
		 */
	}

}
